fruits = ["apples", "mango", "cherry", "banana"]


# * for loop
for fruit in fruits:
    #! end is used to give fruits in one line
    
    print(fruit + ", ", end=""); 
    
  
print("----------------------for loop in range---------------------------")  
  
for i in range(10):
    print(i);


print("-----------------------while loop--------------------------")    
# * while looop 
i = 1
while i < 5:
    print(i, end="")
    i += 1
    